package operators;

public class OperatorDemo {

    public static void main(String[] args) {
        int num = 10;
        System.out.println(num++);//10
        System.out.println(num);//11
        System.out.println(++num);//12
        System.out.println(num--);//12
        System.out.println(num);//11
        System.out.println(--num);//10
        System.out.println(10%3);//1
    }
}
